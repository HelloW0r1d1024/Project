#include<stdio.h>

int isLeap(int y) {
    return y % 4 == 0 && y % 100 != 0 || y % 400 == 0 ? 1 : 0;
}

int days[12] = {
    0,
    31,
    31 + 28,
    31 + 28 + 31,
    31 + 28 + 31 + 30,
    31 + 28 + 31 + 30 + 31,
    31 + 28 + 31 + 30 + 31 + 30,
    31 + 28 + 31 + 30 + 31 + 30 + 31,
    31 + 28 + 31 + 30 + 31 + 30 + 31 + 31,
    31 + 28 + 31 + 30 + 31 + 30 + 31 + 31 + 30,
    31 + 28 + 31 + 30 + 31 + 30 + 31 + 31 + 30 + 31,
    31 + 28 + 31 + 30 + 31 + 30 + 31 + 31 + 30 + 31 + 30
};
int main() {
    int y, m, d;

    while (~scanf("%d/%d/%d", &y, &m, &d)) {
        if (m < 3) {
            printf("%d\n", days[m - 1] + d);
        }
        else 
            printf("%d\n", days[m - 1] + d + isLeap(y));
    }

    return 0;
}